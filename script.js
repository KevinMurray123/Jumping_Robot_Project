let splitInput = input.split("\n")

function doTheThingEpicStyle(array, moveRight, start) {
    let amountOfRobots = 1
    let right = start;

    for (let i = 0; i < array.length; i++) {
        if (right >= 31) {
            right = right % 31;
        }
        if (array[i].charAt(right) == `#`) {
            amountOfRobots++;
        } 
        right += moveRight;
    }
    
    return amountOfRobots;
}

